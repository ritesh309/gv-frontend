// Cart functionality (localStorage based)
export const cartUtils = {
  getCart: () => {
    const cart = localStorage.getItem('gayati-cart');
    return cart ? JSON.parse(cart) : [];
  },
  
  addToCart: (product, quantity = 1) => {
    const cart = cartUtils.getCart();
    const existingItem = cart.find(item => item.id === product.id);
    
    if (existingItem) {
      existingItem.quantity += quantity;
    } else {
      cart.push({ ...product, quantity });
    }
    
    localStorage.setItem('gayati-cart', JSON.stringify(cart));
    return cart;
  },
  
  removeFromCart: (productId) => {
    const cart = cartUtils.getCart();
    const updatedCart = cart.filter(item => item.id !== productId);
    localStorage.setItem('gayati-cart', JSON.stringify(updatedCart));
    return updatedCart;
  },
  
  updateQuantity: (productId, quantity) => {
    const cart = cartUtils.getCart();
    const item = cart.find(item => item.id === productId);
    if (item) {
      item.quantity = quantity;
      localStorage.setItem('gayati-cart', JSON.stringify(cart));
    }
    return cart;
  },
  
  clearCart: () => {
    localStorage.removeItem('gayati-cart');
    return [];
  },
  
  getCartTotal: () => {
    const cart = cartUtils.getCart();
    return cart.reduce((total, item) => total + (item.price * item.quantity), 0);
  },
  
  getCartCount: () => {
    const cart = cartUtils.getCart();
    return cart.reduce((count, item) => count + item.quantity, 0);
  }
};